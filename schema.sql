  'Generated STORED geography(Point, 4326) column derived from latitude/longitude. SQLModel does not declare it; reads happen via raw SQL. Indexed by GiST (ix_address_geo).';

COMMENT ON TABLE "store" IS
  'pin_confirmed=false until seller confirms the map pin in the dashboard. delivery_radius_km gates serviceability checks (PostGIS ST_DWithin).';

COMMENT ON TABLE "order" IS
  'Totals are intentionally denormalized for immutable financial history. delivery_address_snapshot captures a human-readable copy of the address at order time.';

COMMENT ON TABLE "review" IS
  'CHECK ck_review_one_target: exactly one of product_id / store_id must be set. CHECK ck_review_rating_range: rating in 1..5.';

