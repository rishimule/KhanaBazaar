-- KhanaBazaar database schema (Postgres)
-- Source of truth: SQLModel models in backend/app/src/app/models/ + Alembic
-- migration head `cecb3aa39b17`. Regenerate this file when the head changes.
--
-- Enums (created via Alembic migrations):
--   userrole          : 'Customer', 'Seller', 'Admin'
--   verificationstatus: 'Pending', 'Approved', 'Rejected'
--   orderstatus       : 'Pending', 'Paid', 'Packed', 'Dispatched', 'Delivered', 'Cancelled'
--   paymentmethod     : 'Upi', 'Cash'
--   paymentstatus     : 'Pending', 'Paid', 'Failed', 'Refunded'
--   deliverystatus    : 'Pending', 'Packed', 'Dispatched', 'Delivered', 'Cancelled'
--   locationsource    : 'manual', 'autocomplete', 'pin', 'geocoded'
--
-- PostGIS extension is required (CREATE EXTENSION IF NOT EXISTS postgis).
-- `language.code` is plain VARCHAR (PK); the LanguageCode Python enum
-- (en/hi/mr/gu/pa) is enforced at the application layer, not the DB.

CREATE TABLE "language" (
  "code" VARCHAR PRIMARY KEY,
  "name" VARCHAR NOT NULL,
  "native_name" VARCHAR NOT NULL,
  "is_active" BOOLEAN NOT NULL
);

CREATE TABLE "user" (
  "id" INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "created_at" TIMESTAMPTZ NOT NULL,
  "updated_at" TIMESTAMPTZ NOT NULL,
  "email" VARCHAR NOT NULL,
  "hashed_password" VARCHAR,
  "is_active" BOOLEAN NOT NULL,
  "role" userrole NOT NULL,
  "preferred_language" VARCHAR NOT NULL DEFAULT 'en'
);

CREATE TABLE "address" (
  "id" INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "created_at" TIMESTAMPTZ NOT NULL,
  "updated_at" TIMESTAMPTZ NOT NULL,
  "address_line1" VARCHAR(120) NOT NULL,
  "address_line2" VARCHAR(120),
  "landmark" VARCHAR(120),
  "city" VARCHAR(80) NOT NULL,
  "state" VARCHAR(80) NOT NULL,
  "pincode" VARCHAR(10) NOT NULL,
  "country" VARCHAR(60) NOT NULL DEFAULT 'India',
  "latitude" DOUBLE PRECISION,
  "longitude" DOUBLE PRECISION,
  "digipin" VARCHAR(12),
  "place_id" VARCHAR(255),
  "location_source" locationsource,
  -- `geo` is a GENERATED STORED column added by migration 3e36788adecf.
  -- Not declared on the SQLModel; reads happen via raw SQL.
  "geo" geography(Point, 4326) GENERATED ALWAYS AS (
    CASE WHEN latitude IS NOT NULL AND longitude IS NOT NULL
         THEN ST_SetSRID(ST_MakePoint(longitude, latitude), 4326)::geography
         ELSE NULL END
  ) STORED
);

